def print_animal_list(animals_list, title):
    print(f"\n{title}:")
    for name, species, weight in animals_list:
        print(f"  {name:12} ({species:15}) — {weight} kg")


def find_by_mass(animals_list, target_mass):
    result = [a for a in animals_list if a[2] == target_mass]
    return result


def group_by_mass(animals_list):
    groups = {}
    for a in animals_list:
        mass = a[2]
        if mass not in groups:
            groups[mass] = []
        groups[mass].append(a)
    return groups




